// This Java program checks if the sum of two integers is less than 20 and uses an if-else statement to check the condition.
public class If_else {
    public static void main(String[] args) {
        int x = 10;
        int y = 12;

        if (x + y < 20) {
            System.out.println("x + y is less than 20");
        } else {
            System.out.println("x + y is greater than 20");
        }
    }
}
