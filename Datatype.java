// This Java program demonstrates the use of different data types.
public class Datatype {
    public static void main(String[] args) {
        int myNum = 5;    // this is an integer
        float myFloatNum = 5.99F; // this is a floating point number
        char myLetter = 'D';    // this is a character
        boolean myBool = true;  // this is a boolean
        String myText = "Hello"; // this is a string

        System.out.println("Integer: " + myNum);     // this print is an integer
        System.out.println("Float: " + myFloatNum);  // this print is a float
        System.out.println("Character: " + myLetter); // this print is a character
        System.out.println("Boolean: " + myBool);     // this print is a boolean
        System.out.println("String: " + myText);   // this print is a string
    }
}


